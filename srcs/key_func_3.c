#include "fdf.h"

void	switch_iso(t_math *math)
{
	math->bol_rot = (math->bol_rot == 1 ? 0 : 1);
	math->bol_center = 1;
	math->x_rot = 0.523599;
	math->y_rot = 0.523599;
}
